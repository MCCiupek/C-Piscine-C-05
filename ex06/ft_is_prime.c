int	ft_is_prime(int nb)
{
	if ((nb + 1) % 3 != 0 || nb < 2)
		return (0);
	nb = (nb + 1)/3;
	while (nb % 2 == 0 || nb == 1)
	{
		if (nb == 1)
			return (1);
		nb = nb / 2;
	}
	return (0);
}
